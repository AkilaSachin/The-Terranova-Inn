    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed

        try {
            String user_name=username.getText();
            String pass=password.getText();
            boolean b=false;
            boolean c=false;
            this.setVisible(false);

            String que="select * from user_info";
            pst=con.prepareStatement(que);
            rst=pst.executeQuery();
            
            while(rst.next()){
                if(rst.getString("username").equals(user_name)&&  rst.getString("password").equals(pass) && rst.getString("position").equals("admin")){
                    b=true;
                }
                else if(rst.getString("username").equals(user_name)&&  rst.getString("password").equals(pass)){
                    c=true;
                }
                    
            }

                    if(b==true){
                        manager m=new manager();
                        JOptionPane.showMessageDialog(null,"Login as Manager");
                        this.dispose();
                        m.setVisible(true);
                        
                    }
                    if(c==true){
                        reseption r=new reseption();
                        JOptionPane.showMessageDialog(null,"Login as Receptienist");
                        this.dispose();
                        r.setVisible(true);
                    }    

                    else if(b==false && c==false){

                JOptionPane.showMessageDialog(null,"Incorrect Password");
                this.setVisible(true);
            }
        } catch (SQLException ex) {

            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_loginActionPerformed

